# similar to switch/when statement in kotlin

a = int(input("Enter the number: "))

match a:
    case 1:
        print("case is 1")
    case 10:
        print("case is 10")
    case 20:
        print("case is 20")
    case 30:
        print("case is 30")
    case 40:
        print("case is 40")
    case _:
        print("No match found")

