
#Operators
num1 = 10
num2 = 3

print("num1 + num2 is ", num1 + num2)
print("num1 - num2 is ", num1 - num2)
print("num1 * num2 is ", num1 * num2)
print("num1 / num2 is ", num1 / num2)
print("num1 % num2 is ", num1 % num2)
print("num1 ** num2 is ", num1 ** num2) #expnential like: 10 to the power 2
print("num1 // num2 is ", num1 // num2) #float remainders

#Assignment Operators
a = 4 # if we print a then we will get the value of a
a += 2 # if we print a now then we will get the values of both 4 and 2 = 6
print(a)

